// Current language (default: English)
var currentLanguage = 'en';

// Get translation
function t(key) {
    return translations[currentLanguage][key] || translations['en'][key] || key;
}

// Change language
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('brainrot-language', lang);
    
    // Update active button
    document.querySelectorAll('.lang-btn').forEach(function(btn) {
        btn.classList.remove('active');
    });
    var activeBtn = document.querySelector('.lang-btn[data-lang="' + lang + '"]');
    if (activeBtn) activeBtn.classList.add('active');
    
    updatePageLanguage();
    console.log('üåç Language changed to:', lang);
}

// Update all text on page
function updatePageLanguage() {
    // Header
    document.querySelector('h1').textContent = t('title');
    document.querySelector('.subtitle').textContent = t('subtitle');
    
    // Alphabet nav
    var allBtn = document.querySelector('.letter-btn[onclick*="ALL"]');
    if (allBtn) allBtn.textContent = t('allButton');
    
    // Start game button
    var startBtn = document.querySelector('.game-btn');
    if (startBtn) startBtn.textContent = t('startGame');
    
    // Footer
    var footer = document.querySelector('footer p');
    if (footer) footer.textContent = t('footer');
    
    // Game mode selection
    var modeTitle = document.querySelector('.mode-selection-title');
    if (modeTitle) modeTitle.textContent = t('modeTitle');
    
    var chickenName = document.querySelector('.mode-chicken .mode-name');
    if (chickenName) chickenName.textContent = t('modeChicken');
    
    var chaosName = document.querySelector('.mode-chaos .mode-name');
    if (chaosName) chaosName.textContent = t('modeChaos');
    
    var chickenDesc = document.querySelector('.mode-chicken .mode-description');
    if (chickenDesc) chickenDesc.innerHTML = t('modeChickenDesc');
    
    var chaosDesc = document.querySelector('.mode-chaos .mode-description');
    if (chaosDesc) chaosDesc.innerHTML = t('modeChaosDesc');
    
    // Game stats labels
    var scoreLabel = document.querySelector('.game-stat-label');
    if (scoreLabel && scoreLabel.textContent.includes('SCORE')) scoreLabel.textContent = t('score');
    
    // Kaos mode indicator
    var kaosIndicator = document.getElementById('chaos-mode-indicator');
    if (kaosIndicator) kaosIndicator.textContent = t('kaosModeActive');
    
    // Highscore title
    var highscoreTitle = document.querySelector('#highscore-container h2');
    if (highscoreTitle) highscoreTitle.textContent = t('highscoreTitle');
    
    // Re-render characters if loaded
    if (allCharacters.length > 0) {
        renderCharacters();
    }
}

// Load saved language on init
function initLanguage() {
    var savedLang = localStorage.getItem('brainrot-language');
    if (savedLang && translations[savedLang]) {
        currentLanguage = savedLang;
    }
    
    // Set active button
    document.querySelectorAll('.lang-btn').forEach(function(btn) {
        btn.classList.remove('active');
    });
    var activeBtn = document.querySelector('.lang-btn[data-lang="' + currentLanguage + '"]');
    if (activeBtn) activeBtn.classList.add('active');
    
    updatePageLanguage();
}

